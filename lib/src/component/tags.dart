/// The [Tags] enum lists the common HTML tags that can be used as the root element in a component.
enum Tags {
  /// The `<div>` tag is used to group HTML elements together and apply styles.
  div,

  /// The `<span>` tag is used to group inline HTML elements together and apply styles.
  span,

  /// The `<button>` tag is used to create a button element that can trigger actions or events.
  button,
}
