/// {@template Tags}
/// The [Tag] enum lists the common html tags that can be
/// used as the root element in a component.
/// {@endtemplate}
enum Tag {
  /// The `<div>` tag is used to group html elements together and apply styles.
  div,

  /// The `<span>` tag is used to group inline html
  /// elements together and apply styles.
  span,

  /// The `<button>` tag is used to create a button element
  /// that can trigger actions or events.
  button;
}
